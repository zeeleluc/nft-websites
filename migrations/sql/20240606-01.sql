CREATE TABLE nfts (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nft_id INT,
    background VARCHAR(255) NULL,
    back_props VARCHAR(255) NULL,
    body VARCHAR(255) NULL,
    plebs_heads VARCHAR(255) NULL,
    clothes VARCHAR(255) NULL,
    eyes VARCHAR(255) NULL,
    hair VARCHAR(255) NULL,
    hands VARCHAR(255) NULL,
    mouth VARCHAR(255) NULL,
    special VARCHAR(255) NULL,
    accessories VARCHAR(255) NULL,
    hats VARCHAR(255) NULL
);
